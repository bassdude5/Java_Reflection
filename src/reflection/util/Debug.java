package reflection.util;
//---------------------------------------------------------------------
public class Debug
{
	private int debug_val;
	
	/**
	*	The null class constructor
	**/
	public Debug()
	{

	}
	
	/**
	*	This method is used to set the variable debug_val
	**/
	public void setDebugVal(int debug_val)
	{
		this.debug_val = debug_val;
	}

	/**
	*	This method is used to get the variable debug_val
	*	@return returns the int "debug_val"
	**/
	public int getDebugVal()
	{
		return debug_val;
	}
}
